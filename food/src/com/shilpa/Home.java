package com.shilpa;

public class Home {

	private String name;
	private int doorno;
	private String address;

	public Home(String name, int doorno, String address)
	{

		this.name = name;
		this.doorno = doorno;
		this.address = address;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getDoorno() {
		return doorno;
	}

	public void setDoorno(int doorno) {
		this.doorno = doorno;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	
	
	
	
	
	
	
	
}